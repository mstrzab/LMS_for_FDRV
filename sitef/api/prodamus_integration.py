async def process_successful_payment(webhook_data: dict) -> Dict[str, Any]:
    """
    Process a successful payment webhook from Prodamus.
    
    This function:
    1. Verifies the webhook signature
    2. Creates or retrieves the user
    3. Grants access to the purchased course
    4. Sends welcome email with credentials (if new user)
    
    Args:
        webhook_data: Complete webhook data from Prodamus
    
    Returns:
        Dictionary with processing result
    
    Raises:
        ValueError: If signature verification fails
        Exception: If database operations fail
    """
    from database import (
        create_user,
        get_user_by_email,
        purchase_course,
        get_course_by_id,
    )
    
    # Extract signature
    signature = webhook_data.get("sign", "")
    
    # Verify signature
    if not verify_webhook_signature(webhook_data, signature):
        raise ValueError("Invalid webhook signature")
    
    # Extract payment data
    order_id = webhook_data.get("order_id", "")
    status = webhook_data.get("status", "")
    customer_email = webhook_data.get("customer_email", "")
    total_price = webhook_data.get("total_price", 0)  # In kopecks
    
    # Get metadata
    meta = webhook_data.get("meta", {})
    course_id = meta.get("course_id") or webhook_data.get("meta[course_id]")
    
    if not course_id:
        # Try to extract from order_id (format: LMS-...-C{course_id})
        if "C" in order_id:
            try:
                course_id = int(order_id.split("C")[-1])
            except ValueError:
                pass
    
    if not course_id:
        raise ValueError("Course ID not found in payment data")
    
    # Verify payment status
    if status.lower() not in ["success", "paid", "completed"]:
        return {
            "success": False,
            "message": f"Payment status is '{status}', not processing"
        }
    
    # Get course info
    course = get_course_by_id(int(course_id))
    if not course:
        raise ValueError(f"Course {course_id} not found")
    
    # Get or create user
    user = get_user_by_email(customer_email)
    is_new_user = False
    generated_password = None
    
    if not user:
        # Create new user with random password
        generated_password = secrets.token_urlsafe(12)
        user = create_user(
            email=customer_email,
            password=generated_password,
            role="user"
        )
        is_new_user = True
    
    # Grant course access
    try:
        purchase = purchase_course(
            user_id=user["id"],
            course_id=int(course_id),
            payment_id=order_id,
            amount_rub=total_price // 100,  # Convert to rubles
            payment_status="completed"
        )
    except ValueError as e:
        # User already has access
        return {
            "success": True,
            "message": str(e),
            "user_id": user["id"],
            "course_id": course_id
        }
    
    # TODO: Send welcome email with credentials for new users
    # await send_welcome_email(user["email"], generated_password, course["title"])
    
    return {
        "success": True,
        "message": "Payment processed successfully",
        "user_id": user["id"],
        "course_id": course_id,
        "is_new_user": is_new_user,
        "generated_password": generated_password if is_new_user else None
    }


