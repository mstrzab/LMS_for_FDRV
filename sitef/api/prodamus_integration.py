def generate_signature(data: dict, secret_key: str) -> str:
    """
    Generate HMAC-SHA256 signature for Prodamus request.
    
    The signature is generated by:
    1. Sorting parameters alphabetically
    2. Concatenating key-value pairs
    3. Calculating HMAC-SHA256 with secret key
    
    Args:
        data: Dictionary of parameters to sign
        secret_key: Secret key from Prodamus
    
    Returns:
        Hexadecimal signature string
    """
    # Sort parameters alphabetically
    sorted_params = sorted(data.items())
    
    # Concatenate key-value pairs
    signature_string = "&".join(f"{k}={v}" for k, v in sorted_params if v is not None)
    
    # Calculate HMAC-SHA256
    signature = hmac.new(
        secret_key.encode(),
        signature_string.encode(),
        hashlib.sha256
    ).hexdigest()
    
    return signature


def generate_payment_link(
    order_id: str,
    product_name: str,
    price_rub: int,
    customer_email: str,
    course_id: int,
    success_url: str = None,
    fail_url: str = None,
    webhook_url: str = None,
) -> str:
    """
    Generate a Prodamus payment link.
    
    This function creates a payment URL that redirects users to Prodamus
    payment page with pre-filled product and customer information.
    
    Args:
        order_id: Unique order identifier
        product_name: Name of the product (course title)
        price_rub: Price in rubles (will be converted to kopecks)
        customer_email: Customer's email for receipt
        course_id: ID of the course being purchased
        success_url: URL to redirect after successful payment
        fail_url: URL to redirect after failed payment
        webhook_url: URL for payment notifications
    
    Returns:
        Complete payment URL for Prodamus
    
    Example:
        >>> link = generate_payment_link(
        ...     order_id="ORDER-123",
        ...     product_name="Python Course",
        ...     price_rub=4990,
        ...     customer_email="user@example.com",
        ...     course_id=1
        ... )
        >>> print(link)
        https://pay.prodamus.ru/?...
    """
    # Convert price to kopecks
    price_kopecks = price_rub * 100
    
    # Build request parameters
    params = {
        "order_id": order_id,
        "customer_email": customer_email,
        "products[0][name]": product_name,
        "products[0][price]": str(price_kopecks),
        "products[0][quantity]": "1",
    }
    
    # Add optional parameters
    if success_url:
        params["success_url"] = success_url
    if fail_url:
        params["fail_url"] = fail_url
    if webhook_url:
        params["callback_url"] = webhook_url
    
    # Add metadata for webhook processing
    params["meta[course_id]"] = str(course_id)
    params["meta[email]"] = customer_email
    
    # Generate signature
    signature = generate_signature(params, PRODAMUS_CONFIG["secret_key"])
    params["sign"] = signature
    
    # Build URL
    query_string = urllib.parse.urlencode(params, safe="[]")
    payment_url = f"{PRODAMUS_CONFIG['api_url']}?{query_string}"
    
    return payment_url


def generate_payment_link_alternative(
    order_id: str,
    products: list[PaymentProduct],
    customer_email: str,
    meta: Dict[str, Any] = None,
    success_url: str = None,
    fail_url: str = None,
    webhook_url: str = None,
) -> str:
    """
    Alternative method using products list (for multiple items).
    
    Args:
        order_id: Unique order identifier
        products: List of products in the order
        customer_email: Customer's email
        meta: Additional metadata
        success_url: Redirect URL after success
        fail_url: Redirect URL after failure
        webhook_url: Webhook URL for notifications
    
    Returns:
        Complete payment URL
    """
    params = {
        "order_id": order_id,
        "customer_email": customer_email,
    }
    
    # Add products
    for i, product in enumerate(products):
        params[f"products[{i}][name]"] = product.name
        params[f"products[{i}][price]"] = str(product.price)
        params[f"products[{i}][quantity]"] = str(product.quantity)
        if product.sku:
            params[f"products[{i}][sku]"] = product.sku
    
    # Add URLs
    if success_url:
        params["success_url"] = success_url
    if fail_url:
        params["fail_url"] = fail_url
    if webhook_url:
        params["callback_url"] = webhook_url
    
    # Add metadata
    if meta:
        for key, value in meta.items():
            params[f"meta[{key}]"] = str(value)
    
    # Generate signature
    signature = generate_signature(params, PRODAMUS_CONFIG["secret_key"])
    params["sign"] = signature
    
    # Build URL
    query_string = urllib.parse.urlencode(params, safe="[]")
    return f"{PRODAMUS_CONFIG['api_url']}?{query_string}"


# ==========================================
# WEBHOOK SIGNATURE VERIFICATION
# ==========================================

def verify_webhook_signature(data: dict, signature: str, secret: str = None) -> bool:
    """
    Verify webhook signature from Prodamus.
    
    Prodamus sends a signature in the 'sign' parameter. This function
    verifies that the signature matches the expected value.
    
    Args:
        data: Complete webhook data (excluding 'sign')
        signature: Signature from webhook
        secret: Webhook secret (uses config if not provided)
    
    Returns:
        True if signature is valid, False otherwise
    """
    secret = secret or PRODAMUS_CONFIG["webhook_secret"]
    
    # Remove sign from data for verification
    data_copy = {k: v for k, v in data.items() if k != "sign"}
    
    # Calculate expected signature
    expected_signature = generate_signature(data_copy, secret)
    
    # Compare signatures (constant-time comparison)
    return hmac.compare_digest(signature.lower(), expected_signature.lower())


